    #mkstemp() creates and opens a temporary file, returning the file descriptor and path to it
    db_fd, db_path = tempfile.mkstemp()

    #DATABASE path is overwritten to point to temp db path
    #TESTING tells Flask that the app is in test mode
    app = create_app({
        'TESTING': True,
        'DATABASE': db_path,
    })

    with app.app_context():
        init_db()
        get_db().executescript(_data_sql)
    
    yield app #return a generator

    os.close(db_fd)
    os.unlink(db_path)

